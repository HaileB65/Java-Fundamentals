package labs_examples.lambdas.labs.exercise_03;

class MathFunctions{

    public static int xPlusYSquared(int x, int y){
        return x * + (y*y);
    }

    public static int ySqred(int x){
        return x*x;
    }

    @Override
    public String toString() {
        return "MathFunctions{}";
    }
}
